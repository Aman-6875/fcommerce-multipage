            $product = $products[0];
            $template = $labels['success_single'] ?? ($language === 'bn' 
                ? "тЬЕ ржирж┐рж░рзНржмрж╛ржЪрж┐ржд: {product}" 
                : "тЬЕ Selected: {product}");
            return str_replace('{product}', $product['name'], $template);
        }

        $template = $labels['success_multiple'] ?? ($language === 'bn' 
            ? "тЬЕ {count}ржЯрж┐ ржкржгрзНржп ржирж┐рж░рзНржмрж╛ржЪрж┐ржд" 
            : "тЬЕ Selected {count} products");
